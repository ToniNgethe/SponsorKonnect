package Model;

/**
 *
 * @author toni
 */
public class WorkerModel {
    
    private int id;
    private String name, number, email, location, password;

    public WorkerModel() {
    }

    public WorkerModel(String name, String number, String email, String location, String password) {
        this.name = name;
        this.number = number;
        this.email = email;
        this.location = location;
        this.password = password;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
}
